  for i, r in adjacent:
    for j, a in r:
      assert adjacent[j][i] == a, fmt"{i+1}, {j+1} is {adjacent[i][j]} but {j+1}, {i+1} is {adjacent[j][i]}"

  for i in pents:
    r = adjacent[i]
    t = r.foldl(a + int(b), 0)
    assert t == 5, fmt"pentagon {i+1} is adjacent to {t} faces"
  for i in tris:
